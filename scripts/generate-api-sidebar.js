/**
 * Generate API sidebar structure from OpenAPI spec
 * This script reads the swagger.json file and generates a properly structured
 * sidebar configuration for sidebars.ts that groups endpoints by OpenAPI tags.
 * 
 * Usage: node scripts/generate-api-sidebar.js
 */

const fs = require('fs');
const path = require('path');

const SWAGGER_PATH = path.join(__dirname, '../sources/swagger.json');
const API_DOCS_DIR = path.join(__dirname, '../docs/api');

// Read swagger spec
const swagger = JSON.parse(fs.readFileSync(SWAGGER_PATH, 'utf8'));
const tags = swagger.tags || [];
const paths = swagger.paths || {};

// Get all API doc files
const apiFiles = fs.readdirSync(API_DOCS_DIR)
  .filter(f => f.endsWith('.api.mdx'))
  .map(f => f.replace('.api.mdx', ''));

console.log(`Found ${apiFiles.length} API endpoint files`);

// Group files by tag based on the OpenAPI spec
const tagGroups = {};
tags.forEach(tag => {
  tagGroups[tag.name] = [];
});

Object.keys(paths).forEach(pathStr => {
  Object.keys(paths[pathStr]).forEach(method => {
    const operation = paths[pathStr][method];
    const operationId = operation.operationId;
    const operationTags = operation.tags || [];
    
    // Convert operation ID to filename format
    // The plugin uses kebab-case with v-1 format and oauth-2
    const fileName = operationId
      .replace(/([A-Z])/g, '-$1')
      .toLowerCase()
      .replace(/^-/, '')
      .replace(/v1/, 'v-1')
      .replace(/oauth2/, 'oauth-2');
    
    operationTags.forEach(tag => {
      if (tagGroups[tag] && apiFiles.includes(fileName)) {
        tagGroups[tag].push(`'api/${fileName}'`);
      }
    });
  });
});

// Build the sidebar structure as TypeScript code
const sidebarLines = [
  '  // API Reference sidebar - manually structured by OpenAPI tags',
  '  // Organized into collapsible groups based on API tags from swagger.json',
  '  // Auto-generated by: npm run generate-api-sidebar',
  '  apiSidebar: [',
  '    {',
  "      type: 'doc',",
  "      id: 'api/emailengine-api',",
  "      label: 'API Overview',",
  '    },'
];

tags.forEach(tag => {
  const items = tagGroups[tag.name];
  if (items && items.length > 0) {
    sidebarLines.push('    {');
    sidebarLines.push("      type: 'category',");
    sidebarLines.push(`      label: '${tag.name}',`);
    sidebarLines.push('      collapsed: true,');
    
    if (items.length === 1) {
      sidebarLines.push(`      items: [${items[0]}],`);
    } else {
      sidebarLines.push('      items: [');
      items.forEach((item, idx) => {
        const comma = idx < items.length - 1 ? ',' : '';
        sidebarLines.push(`        ${item}${comma}`);
      });
      sidebarLines.push('      ],');
    }
    
    sidebarLines.push('    },');
  }
});

sidebarLines.push('  ],');

const output = sidebarLines.join('\n');

console.log('\n' + '='.repeat(80));
console.log('API Sidebar Configuration (TypeScript)');
console.log('='.repeat(80));
console.log('\nCopy this into sidebars.ts to replace the apiSidebar definition:\n');
console.log(output);
console.log('\n' + '='.repeat(80));
console.log(`\nSummary:`);
console.log(`- ${tags.length} tag categories`);
console.log(`- ${apiFiles.length} total endpoints`);
tags.forEach(tag => {
  const count = tagGroups[tag.name].length;
  console.log(`  - ${tag.name}: ${count} endpoints`);
});
console.log('='.repeat(80) + '\n');

